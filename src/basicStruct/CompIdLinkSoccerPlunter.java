package basicStruct;

public class CompIdLinkSoccerPlunter {

	private int compId;
	private String compLink;

	public CompIdLinkSoccerPlunter() {
		super();
	}

	public CompIdLinkSoccerPlunter(int compId, String compLink) {
		super();
		this.compId = compId;
		this.compLink = compLink;
	}

	public int getCompId() {
		return compId;
	}

	public void setCompId(int compId) {
		this.compId = compId;
	}

	public String getCompLink() {
		return compLink;
	}

	public void setCompLink(String compLink) {
		this.compLink = compLink;
	}

}
